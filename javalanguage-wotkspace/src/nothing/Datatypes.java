package nothing;

public class Datatypes {
    public static void main(String args[])
    {
        //example of datatypes in java
        int a=10;
        int b=25;
        int sum=a+b;
        System.out.println(sum);
        int diff=a-b;
        System.out.println(diff);
        int pro=a*b;
        System.out.println(pro);
        int rem=a/b;
        System.out.println(rem);
    }
}
